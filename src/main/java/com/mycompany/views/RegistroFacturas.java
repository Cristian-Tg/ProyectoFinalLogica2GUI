    private void btnfacturarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnfacturarActionPerformed
        String identificacion = this.txtIdentificacion.getText();
        int mesSeleccionado = this.jcbMes.getSelectedIndex();
        try {
            Cliente cliente = modeloCliente.obtener(identificacion);
            ListaContratos contratos = modeloContrato.filtrarCliente(identificacion);
            for (int i = 0; i < contratos.total(); i++) {
                Contrato contrato = contratos.obtener(i);
                if (getValidez(contrato.getFechaPrestacion(), contrato.getDuracion()).contains(mesSeleccionado) ) {
                    modelo.agregarFactura(new Factura(cliente.getIdentificacion(), contrato.getValorMensual(),mesSeleccionado, contrato.getCodigo()));
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, String.format("El cliente con ID %s no se encuentra registrado", identificacion));
        }
    }//GEN-LAST:event_btnfacturarActionPerformed

    public static List<Integer> getValidez(Date fecha, int duracion){
        Integer [] validez = new Integer [duracion];
        for (int i = 0; i < validez.length; i++) {
            validez[i] = (fecha.getMonth() + i) % 12;
        }
        return Arrays.asList(validez);
    }

